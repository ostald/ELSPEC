                                   'ppdir',ppdir,...
                                   'experiment',experiment,...
                                   'hmax',hmax,'hmin',hmin,...
                                   'btime',btime,'etime',etime,...
                                   'ionomodel',ionomodel,...
                                   'integtype',integtype,...
                                   'egrid',egrid,...
                                   'tres',tres,...
                                   'ErrType',ErrType,...
                                   'MaxOrder',maxorder,...
                                   'ninteg',ninteg,... 
                                   'Outfilename',Outname);

ElSpecPlot(ElSpecQT_20070119_L)
[fnm1,fnm2,fnm3] = fileparts(ElSpecQT_20070119_L.Outfilename); 
print('-dpng,'-vector',[fnm2,'-QT-l-5']);
